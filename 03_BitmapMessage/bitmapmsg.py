# Implementation of Bitmap Message
# Displays a user input text in a given bitmap by replacing symbols in the bitmap with the given input symbols.

bitmap = """
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
*************************************************************
"""


replace_with = 'A'

for i in bitmap:
    if i=='*':
        print(replace_with, end="")
    else:
        print(i, end="")
